package Prácticas;

public class Cadena {

	public static void main(String[] args) {
		String palabra = "Hola:D"; //ESTA ES UNA CADENA DE CARACTER
		char x = palabra.charAt(0); //CHARAT MUESTRA UNA LETRA DE UNA POSICIÓN EN UNA PALABRA
		//POR EJEMPLO EN LA PALABRA HOLA, LA POSICION 0 LA OCUPA LA LETRA H
		System.out.println(x);
		System.out.println(palabra);
	}
}